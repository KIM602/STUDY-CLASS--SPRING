package com.kyk.ajaxajax02;

public class Jamong {
	private String name;
	private int age;
	
	public Jamong() {
		super();
	}
	
	public Jamong(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
